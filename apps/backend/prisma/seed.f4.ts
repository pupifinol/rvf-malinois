interface SeededSensor {
  id: string;
  unitCode: string;
  instrumentTag: string;
  canonicalTagName: string;
}

async function seedSensorsAndTransmitters(
  tenantId: string,
  units: Map<string, { id: string; code: string }>,
): Promise<SeededSensor[]> {
  const seeded: SeededSensor[] = [];
  for (const unitDef of UNITS) {
    const unit = units.get(unitDef.code);
    if (!unit) throw new Error(`Unit ${unitDef.code} not seeded`);
    for (const sd of unitDef.sensors) {
      // Sensors: no natural unique constraint in schema; lookup by (unitId, instrumentTag).
      let sensor = await prisma.sensor.findFirst({
        where: { unitId: unit.id, instrumentTag: sd.instrumentTag },
      });
      if (sensor) {
        record('reused', 'sensor');
      } else {
        sensor = await prisma.sensor.create({
          data: {
            tenantId,
            unitId: unit.id,
            type: sd.sensorType,
            name: sd.name,
            instrumentTag: sd.instrumentTag,
            enabled: true,
            minRange: sd.minRange,
            maxRange: sd.maxRange,
            engineeringUnit: sd.engineeringUnit,
          },
        });
        record('created', 'sensor');
      }

      // TransmitterDevice: no natural unique; lookup by (sensorId, serial_number).
      const transmitterSerial = `TX-${sd.instrumentTag}`;
      const existingTx = await prisma.transmitterDevice.findFirst({
        where: { sensorId: sensor.id, serialNumber: transmitterSerial },
      });
      if (existingTx) {
        record('reused', 'transmitter_device');
      } else {
        await prisma.transmitterDevice.create({
          data: {
            tenantId,
            sensorId: sensor.id,
            serialNumber: transmitterSerial,
            manufacturer: sd.transmitterManufacturer,
            model: sd.transmitterModel,
            protocol: sd.transmitterProtocol,
            signalType: sd.transmitterSignalType,
            firmwareVersion: '1.0.0',
            calibrationDate: SEED_DATE,
            calibrationRangeMin: sd.minRange,
            calibrationRangeMax: sd.maxRange,
            calibrationReference: 'F4.3 reference seed',
            installationStatus: 'installed',
            installedAt: SEED_DATE,
          },
        });
        record('created', 'transmitter_device');
      }

      seeded.push({
        id: sensor.id,
        unitCode: unitDef.code,
        instrumentTag: sd.instrumentTag,
        canonicalTagName: sd.canonicalTagName,
      });
    }
  }
  log(`sensors + transmitters present: ${String(seeded.length)} pairs.`);
  return seeded;
}

async function seedSensorTagBindings(
  tenantId: string,
  sensors: SeededSensor[],
  canonicalTags: Map<string, { id: string }>,
): Promise<void> {
  for (const s of sensors) {
    const tag = canonicalTags.get(s.canonicalTagName);
    if (!tag)
      throw new Error(`Canonical tag ${s.canonicalTagName} missing for sensor ${s.instrumentTag}`);
    // Idempotency: a sensor has at most one ACTIVE binding (effective_to IS NULL),
    // enforced by partial unique index. Reuse if it points at the same canonical tag.
    const activeBinding = await prisma.sensorTagBinding.findFirst({
      where: { sensorId: s.id, effectiveTo: null },
    });
    if (activeBinding) {
      if (activeBinding.canonicalTagId === tag.id) {
        record('reused', 'sensor_tag_binding');
        continue;
      }
      // Re-binding: close the existing binding and open a new one.
      await prisma.sensorTagBinding.update({
        where: { id: activeBinding.id },
        data: { effectiveTo: SEED_DATE },
      });
    }
    await prisma.sensorTagBinding.create({
      data: {
        tenantId,
        sensorId: s.id,
        canonicalTagId: tag.id,
        effectiveFrom: SEED_DATE,
        effectiveTo: null,
      },
    });
    record('created', 'sensor_tag_binding');
  }
  log(`sensor-tag bindings present: ${String(sensors.length)} active bindings.`);
}

