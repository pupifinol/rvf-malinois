/**
 * RVF Malinois — F4.3 reference seed.
 *
 * Phase F4.3 — Seed / Reference Data.
 *
 * Strategy reference:
 *   docs/architecture/RVF_Malinois_F4_3_Seed_Reference_Data_Report.md
 * Schema source of truth:
 *   apps/backend/prisma/schema.prisma                                      (commit e37f7b5)
 *   apps/backend/prisma/migrations/20260524000000_f4_2_baseline/migration.sql (commit e37f7b5)
 *   database/schema/RVF_Malinois_F4_1_PostgreSQL_Schema.sql                 (commit a475066)
 *
 * Goals:
 *   - Populate the minimum reference data needed to unblock F4.4 (API
 *     adaptation) and F4.6 (telemetry persistence).
 *   - Demonstrate that operating envelopes and alarm rules are PER-UNIT,
 *     not global (HP-001 vs LP-001).
 *   - Stay idempotent: running this seed twice must not duplicate rows.
 *
 * Non-goals (out of scope per F4.3):
 *   - Real users, real authentication, real credentials.
 *   - Telemetry rows in telemetry_readings.
 *   - Alarm events.
 *   - Integration with ThingsBoard / Node-RED / MQTT / OPC-UA.
 *   - Reactivating quarantined backend modules.
 *
 * Engineering unit conventions used by this seed (documented for F4.4 / F4.6):
 *   - pressure / differential pressure : psi
 *   - temperature                      : degF
 *   - liquid / oil / water flow        : bpd
 *   - gas flow / gas rate              : MMSCFD (Million Standard Cubic Feet per Day)
 *   - liquid volume totals             : bbl
 *   - gas volume totals                : MMSCF
 *   - level                            : %
 *   - vibration                        : in/s
 *   - battery_status, signal_quality   : %
 *   - device_status                    : state (string)
 *
 * Idempotency strategy:
 *   - For models with a natural UNIQUE constraint (canonical_tags.name,
 *     equipment_types.name, measurement_units.(tenant_id, code),
 *     unit_configurations.(unit_id, version),
 *     unit_operating_envelopes.(unit_id, version),
 *     alarm_rules.(unit_id, canonical_tag_id, severity, version),
 *     integration_mappings.(integration_source_id, external_identifier)):
 *     use prisma.<model>.upsert(...).
 *   - For models without a natural UNIQUE constraint (tenants, users, wells,
 *     jobs, sensors, transmitter_devices, sensor_tag_bindings,
 *     commissioning_snapshots, integration_sources, audit_logs):
 *     use findFirst(...) keyed on a deterministic descriptive field, then
 *     create only if missing.
 *   - UUIDs are NEVER hard-coded; ids are generated by gen_random_uuid()
 *     and remain stable once written. Re-running the seed reuses the
 *     pre-existing rows.
 *
 * How to run locally (no destructive defaults):
 *   1. docker compose up -d postgres
 *   2. pnpm --filter @rvf/backend exec prisma migrate dev  # applies F4.2 baseline
 *   3. pnpm --filter @rvf/backend run prisma:seed:f4
 */

