async function seedAlarmRules(
  tenantId: string,
  units: Map<string, { id: string; code: string }>,
  canonicalTags: Map<string, { id: string; name: string }>,
  createdById: string,
): Promise<number> {
  let count = 0;
  const planned: Array<{ unitCode: string; rules: AlarmRuleDef[] }> = [
    { unitCode: 'HP-001', rules: HP001_ALARM_RULES },
    { unitCode: 'LP-001', rules: LP001_ALARM_RULES },
  ];
  for (const { unitCode, rules } of planned) {
    const unit = units.get(unitCode);
    if (!unit) throw new Error(`Unit ${unitCode} missing for alarm rules`);
    for (const r of rules) {
      const tag = canonicalTags.get(r.canonicalTagName);
      if (!tag)
        throw new Error(
          `Canonical tag ${r.canonicalTagName} missing for alarm rule on ${unitCode}`,
        );
      const thresholdFields: Prisma.AlarmRuleUncheckedCreateInput = {
        tenantId,
        unitId: unit.id,
        canonicalTagId: tag.id,
        severity: r.severity,
        enabled: true,
        version: 1,
        isCurrent: true,
        messageTemplate: r.message,
        createdBy: createdById,
      };
      if (r.thresholdKind === 'high') {
        thresholdFields.highThreshold = r.value;
      } else {
        thresholdFields.highHighThreshold = r.value;
      }
      await prisma.alarmRule.upsert({
        where: {
          unitId_canonicalTagId_severity_version: {
            unitId: unit.id,
            canonicalTagId: tag.id,
            severity: r.severity,
            version: 1,
          },
        },
        update: {
          enabled: true,
          isCurrent: true,
          messageTemplate: r.message,
          createdBy: createdById,
          // Reset the threshold fields so re-running with a different value
          // converges. Only the field for this rule's kind carries a value.
          highThreshold: r.thresholdKind === 'high' ? r.value : null,
          highHighThreshold: r.thresholdKind === 'high_high' ? r.value : null,
        },
        create: thresholdFields,
      });
      count++;
    }
  }
  record('created', `alarm_rule:upserted_x${String(count)}`);
  log(`alarm rules present (version 1, is_current=true): ${String(count)} per-unit rules.`);
  return count;
}

interface SeededWellJobSnapshot {
  well: { id: string; name: string };
  job: { id: string };
  snapshot: { id: string } | null;
}

async function seedWellJobSnapshot(
  tenantId: string,
  unitId: string,
  unitCode: string,
  unitSensors: SeededSensor[],
  unitAlarmRules: AlarmRuleDef[],
  envelope: OperatingEnvelopeData,
  engineerId: string,
): Promise<SeededWellJobSnapshot> {
  // Well: no natural unique. Lookup by (tenantId, name).
  const wellName = 'Reference Well A';
  let well = await prisma.well.findFirst({ where: { tenantId, name: wellName } });
  if (well) {
    record('reused', 'well');
  } else {
    well = await prisma.well.create({
      data: {
        tenantId,
        name: wellName,
        fieldOrSite: 'Reference Field',
        location: 'Local Dev',
        type: 'test',
        fluid: 'multiphase',
        designLimits: {
          max_pressure_psi: envelope.maxPressure,
          max_temperature_degF: envelope.maxTemperature,
          max_liquid_flow_bpd: envelope.maxFlowRate,
          max_gas_flow_MMSCFD: envelope.maxGasRate,
        } satisfies Prisma.InputJsonValue,
      },
    });
    record('created', 'well');
  }

  // Job: no natural unique. Lookup by (tenantId, wellId, unitId, startedAt).
  const jobStartedAt = SEED_DATE;
  let job = await prisma.job.findFirst({
    where: { tenantId, wellId: well.id, unitId, startedAt: jobStartedAt },
  });
  if (!job) {
    job = await prisma.job.create({
      data: {
        tenantId,
        wellId: well.id,
        unitId,
        engineerId,
        status: 'in_progress',
        startedAt: jobStartedAt,
        // commissioning_snapshot_id stays null until the snapshot is created.
      },
    });
    record('created', 'job');
  } else {
    record('reused', 'job');
  }

  // CommissioningSnapshot: one per job is the seed's contract. Look up by jobId.
  let snapshot = await prisma.commissioningSnapshot.findFirst({ where: { jobId: job.id } });
  if (!snapshot) {
    const sensorMappings = unitSensors.map((s) => ({
      instrument_tag: s.instrumentTag,
      canonical_tag: s.canonicalTagName,
    }));
    const effectiveThresholds = unitAlarmRules.map((r) => ({
      canonical_tag: r.canonicalTagName,
      severity: r.severity,
      kind: r.thresholdKind,
      value: r.value,
    }));
    const engineeringEnvelope = {
      max_pressure: envelope.maxPressure,
      max_flow_rate: envelope.maxFlowRate,
      max_temperature: envelope.maxTemperature,
      max_vibration: envelope.maxVibration,
      max_differential_pressure: envelope.maxDifferentialPressure,
      max_volume: envelope.maxVolume,
      max_gas_rate: envelope.maxGasRate,
      engineering_unit_set: ENGINEERING_UNIT_SET,
    };
    const ruleVersions = unitAlarmRules.map((r) => ({
      canonical_tag: r.canonicalTagName,
      severity: r.severity,
      version: 1,
    }));
    snapshot = await prisma.commissioningSnapshot.create({
      data: {
        tenantId,
        jobId: job.id,
        unitId,
        takenAt: SEED_DATE,
        effectiveThresholds: effectiveThresholds satisfies Prisma.InputJsonValue,
        sensorMappings: sensorMappings satisfies Prisma.InputJsonValue,
        engineeringEnvelope: engineeringEnvelope satisfies Prisma.InputJsonValue,
        ruleVersions: ruleVersions satisfies Prisma.InputJsonValue,
        immutable: true,
      },
    });
    record('created', 'commissioning_snapshot');

    // Close the circular FK: point the job at the freshly-created snapshot.
    await prisma.job.update({
      where: { id: job.id },
      data: { commissioningSnapshotId: snapshot.id },
    });
  } else {
    record('reused', 'commissioning_snapshot');
  }

  log(
    `well + job + snapshot present for ${unitCode}: well="${well.name}", job=${job.id}, snapshot=${snapshot.id}.`,
  );
  return { well, job, snapshot };
}

