async function seedTenant(): Promise<{ id: string; name: string }> {
  const existing = await prisma.tenant.findFirst({ where: { name: TENANT_NAME } });
  if (existing) {
    record('reused', 'tenant');
    log(`tenant "${existing.name}" already present (${existing.id}); reusing.`);
    return existing;
  }
  const created = await prisma.tenant.create({
    data: {
      name: TENANT_NAME,
      status: 'active',
      residencyHint: 'local-dev',
    },
  });
  record('created', 'tenant');
  log(`tenant "${created.name}" created (${created.id}).`);
  return created;
}

interface SeededUsers {
  systemUser: { id: string; displayName: string };
  adminUser: { id: string; displayName: string };
}

async function seedUsers(tenantId: string): Promise<SeededUsers> {
  async function ensure(
    displayName: string,
    role: string,
  ): Promise<{ id: string; displayName: string }> {
    const existing = await prisma.user.findFirst({ where: { tenantId, displayName, role } });
    if (existing) {
      record('reused', 'user');
      return existing;
    }
    const created = await prisma.user.create({
      data: {
        tenantId,
        displayName,
        role,
        status: 'active',
      },
    });
    record('created', 'user');
    return created;
  }
  const systemUser = await ensure('System User', 'system');
  const adminUser = await ensure('Admin Placeholder', 'admin');
  log(`users present: ${systemUser.displayName}, ${adminUser.displayName}.`);
  return { systemUser, adminUser };
}

