interface SeedCounters {
  created: Record<string, number>;
  reused: Record<string, number>;
}

const counters: SeedCounters = { created: {}, reused: {} };

function record(kind: 'created' | 'reused', entity: string): void {
  counters[kind][entity] = (counters[kind][entity] ?? 0) + 1;
}

function log(message: string): void {
  // Plain console.log — this is a CLI seed; no Pino in scope.

  console.log(`[F4.3 seed] ${message}`);
}

